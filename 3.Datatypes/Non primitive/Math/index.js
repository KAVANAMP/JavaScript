//Math is a built-in object that provides properties and methods for mathematical operations.
//Math is not a data type. It is a built-in object.

//Math.round() -> Rounds a no to the nearest integer
console.log(Math.round(4.6)); //5
console.log(Math.round(4.3)); //4

//Math.ceil() ->Always rounds up to the next integer.
console.log(Math.ceil(3.1)); //4
console.log(Math.ceil(6.7)); //7

//Math.floor() ->Always rounds down to the previous integer
console.log(Math.floor(9.4)); //9
console.log(Math.floor(4.1)); //4

//Math.trunc() -> Removes decimal part
console.log(Math.trunc(2.67)); //2

//Math.abs() -> Returns absolute values(removes -ve sign)
console.log(Math.abs(-25)); //25

//Math.pow() -> returns the power of a no
//syntax - Math.pow(base, exponent)
console.log(Math.pow(2,3)); //8

//Math.sqrt() ->returns the squreroot
console.log(Math.sqrt(25)); //5

//Math.max() ->returns largest values
console.log(Math.max(90,45,37)); //90

//Math.min() -> returns the smallest value
console.log(Math.min(90,45,37)); //37

//Math.random() -> generates a random no
console.log(Math.random());

